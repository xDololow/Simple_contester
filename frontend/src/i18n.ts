type I18nValue = {
  language: UiLanguage;
  setLanguage: (language: UiLanguage) => void;
  t: (key: string, vars?: Vars) => string;
};

const I18nContext = createContext<I18nValue | null>(null);

function readLanguage(): UiLanguage {
  if (typeof localStorage === "undefined") return "ru";
  return localStorage.getItem(STORAGE_KEY) === "en" ? "en" : "ru";
}

function interpolate(value: string, vars?: Vars) {
  if (!vars) return value;
  return value.replace(/\{(\w+)\}/g, (_, key: string) => String(vars[key] ?? `{${key}}`));
}

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguageState] = useState<UiLanguage>(() => readLanguage());

  const setLanguage = useCallback((nextLanguage: UiLanguage) => {
    localStorage.setItem(STORAGE_KEY, nextLanguage);
    setLanguageState(nextLanguage);
  }, []);

  const t = useCallback(
    (key: string, vars?: Vars) => interpolate(dictionaries[language][key] ?? dictionaries.ru[key] ?? key, vars),
    [language]
  );

  const value = useMemo(() => ({ language, setLanguage, t }), [language, setLanguage, t]);

  return React.createElement(I18nContext.Provider, { value }, children);
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) throw new Error("useI18n must be used inside I18nProvider");
  return context;
}
